    private void jPanel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel2MouseClicked
        
        if (txt_entidad.getText().trim().isEmpty() || txt_contra.getText().trim().isEmpty() || txt_contra2.getText().trim().isEmpty()) {
            
            JOptionPane.showMessageDialog(null, "Por favor llene todos los campos", "Campos Incompletos", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        try {

            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/inventario", "root", "");
            PreparedStatement pst = cn.prepareStatement("insert into usuarios values(?,?,?)");

            String contrase単aIngresada = new String(txt_contra.getPassword());
            
            pst.setString(1, "00");
            pst.setString(2, txt_entidad.getText().trim());
            pst.setString(3, contrase単aIngresada);
            pst.executeUpdate();
            
            txt_contra2.setText("");
            txt_entidad.setText("");
            txt_contra.setText("");

            respuesta.setText("REGISTRO EXITOSO");

            Timer timer = new Timer(2000, new ActionListener() {
                public void actionPerformed(ActionEvent e) {
                    respuesta.setText("");
                }
            });
            timer.setRepeats(false);
            timer.start();
            
        } catch (Exception e) {
             e.printStackTrace();
        }

    }//GEN-LAST:event_jPanel2MouseClicked

